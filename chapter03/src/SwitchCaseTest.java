/*
分支结构之Switch-case


*/


class SwitchCaseTest{
	public static void main(String[] args) 
	{
		int num = 6;
		switch(num) {
			default:
				System.out.println("other");
			case 0:
				System.out.println("zero");
			case 1:
				System.out.println("one");
			case 2:
				System.out.println("two");
			case 3:
				System.out.println("three");
			
	}
}
}