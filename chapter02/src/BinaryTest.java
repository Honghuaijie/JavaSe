class BinaryTest{
	public static void main(String[] args) 
	{
		int num1 = 103; // 十进制
		int num2 = 0b101; //二进制
		int num3 = 0102;  //八进制
		int num4 = 0x16;  //十六

		System.out.println("num1=" + num1);
		System.out.println("num2=" + num2);
		System.out.println("num3=" + num3);
		System.out.println("num4=" + num4);
	}
}
