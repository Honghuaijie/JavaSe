/*
案例1：定义圆周率并赋值为3.14，现有3个圆的半径分别为1.2、2.5、6，求他们的面积。

*/

class FloatDoubleExer {
	public static void main(String[] args) {
		double PI = 3.14;
		double r1,r2,r3;
		r1=1.2;
		r2=2.5;
		r3=6;
		System.out.println("s1= " + PI * r1 * r1);
		System.out.println("s2= " + PI * r2 * r2);
		System.out.println("s3= " + PI * r3 * r3);
	}
}
